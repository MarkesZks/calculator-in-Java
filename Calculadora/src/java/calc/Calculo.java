public class Calculo {
    //metodos
      public int somar(int n1,int n2){
        return(n1+n2);
    }  
      public int subt(int n1,int n2){
        return(n1-n2);
    } 
       public int mult(int n1,int n2){
        return(n1*n2);
    } 
       
       public int divid (int n1,int n2){
        return(n1/n2);
    } 
       public int dobro(int n1){
        return(n1*2);
    } 
      public int trip(int n1){
        return(n1*3);
    }
      public int cubo (int n1){
        return(n1*n1*n1);
    } 
       
       
    
}
